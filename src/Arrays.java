public class Arrays {
//Arrays are lists that store data of the same datatype
public static void main(String [] args){
    //Creating an array of numbers
    int[] nums = new int[5];
    nums[0] = 1;
    nums[1] = 4;
    nums[2] = 5;
    nums[3] = 7;
    System.out.println(nums[1]);
    String[] names = {"Noel", "Hans", "John","mary"};
    String name = names[0];
    System.out.println(name.length());//the length method is used to print the length of the character that is printed
}
}
