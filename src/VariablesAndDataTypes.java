public class VariablesAndDataTypes {

  public static void main(String [] args){
      //We can declare variables in java following the following methods or wasys
      int age = 20;//this is a number
      char grade = 'A';//Char holds only a single character
      String name = "Noel";//String can hold multiple characters ie more than one

      double pi = 3.14;// double is used to declare floating point numbers that contain a .000 something
      //Type casting, converting data type s from one form to another
      int age2 = (int) pi;
      String age3 = String.valueOf(age);//String conversion

      //Printing the output
      System.out.println(age3);
  }


}
